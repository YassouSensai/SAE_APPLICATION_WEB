                <h3 style="color: #FFFFFF">Introduction</h3>
                <p>Le RGPD (Règlement Général sur la Protection des Données) est un règlement européen entré en vigueur le 25 mai 2018. Il a été conçu en ayant pour objectif la protection des données personnelles des citoyens de l'Union européenne et pour harmoniser les règles de protection des données au sein de l'UE. L’ancienne loi sur la protection des données de 1995 a donc été remplacée par le RGPD qui est plus adapté au contexte actuel d’une part parce qu’elle est appliqué de la même manière dans chaque pays inclu dans l’union européenne, d’autre part, l'intégration des règles de protection des données se fait dès la conception des divers sites internet et logiciels. Enfin, car les amendes en cas de non-respect sont bien plus importantes ce qui poussent les petites et grandes entreprises du marché à respecter la vie privée des citoyens européen avec plus de sérieux. Finalement le RGPD a également pour objectif de répondre aux défis posés par l'évolution rapide des technologies de l'information et de la communication.
                    <br>
                    <br>
                    Le RGPD définit donc plusieurs règles et but à atteindre dans l’application de celui-ci, dont :
                    <br>
                    <br>
                    La protection des données personnelles : Le RGPD garantit que les informations personnelles des individus soient traitées de manière licite, loyale et transparente au regard de la personne concernée.
                    Le renforcement des droits des individus : Il accorde aux internautes plus de contrôle sur leurs données personnelles. En d’autre termes, grâce au RGPD, ils ont le droit de connaître quelles données sont collectées, quelles en sont leur utilisation, de demander l'accès à leurs données, de les corriger, de les supprimer (dans certaines conditions), et de s'opposer (éventuellement) à leur utilisation.
                    Rendre les organisations plus responsables : Les organisations qui traitent les données personnelles de ses utilisateurs sont tenues de respecter des obligations strictes dans le cadre de la protection des données. Elles doivent adopter des pratiques de “privacy by design” et “security by default”, ce qui signifie que la protection des données doit être prise en compte dès la conception des diﬀérents services.
                    Une réglementation à respecter absolument : Le RGPD introduit également des sanctions plus sévères notamment délivrées par la CNIL (Commission nationale de
                    <br>
                    <br>
                    l’Informatique et des libertés). Les entreprises qui ne se conforment pas aux règles de protection des données risquent des amendes considérables, pouvant atteindre jusqu'à 4 % du chiﬀre d'aﬀaires annuel mondial de l'entreprise. De plus, le RGPD oblige les entreprises à notifier les violations de données personnelles aux autorités et personnes concernées après avoir pris connaissance de l'incident.
                    <br>
                    <br>
                    Exemple : Facebook écope d’une amende de 1,2 milliards d’euros pour violation du règlement général sur la protection des données personnelles (source: Le monde informatique)
                    <br>
                    <br>
                    Pour résumer, le RGPD a pour but de renforcer la protection des données personnelles, d'accorder aux individus un plus grand contrôle sur les informations, et de rendre les organisations qui traitent ces données davantage responsables. Il a et a eu par le passé un impact significatif sur la gestion et la protection des données personnelles par les entreprises, et il vise à favoriser la confiance dans le marché de l’informatique et du numérique. En découle du RGPD plusieurs principes ayant pour but la confidentialité des données dont le principe de “privacy by design” qui implique une prise en compte de la protection des données des utilisateurs avant de concevoir les applications, logiciels et sites. ( Selon l’article 25 du RGPD et d’après <a href="https://www.legalplace.fr/guides/privacy-by-design-rgpd/" style="color: #a9a9a9">legalPlace</a> )</p>
