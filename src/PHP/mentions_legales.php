            <h3>2 )Cartographie du traitement des données personnelles</h3>

            <p>“Pour mesurer concrètement l’impact du règlement européen sur la protection des données de votre activité, commencez par recenser de façon précise les traitements de données personnelles que vous mettez en œuvre. La tenue d’un registre des traitements vous permet de faire le point.” Telles sont les recommandations de la CNIL ( <a href="https://www.cnil.fr/fr/cartographier-vos-traitements-de-donnees-personnelles" style="color: #a9a9a9">CNIL - Cartographie</a> ) concernant l’application du principe de Privacy by design pour la conception et le développement de notre application web. Ainsi, nous vous faisons part de la nature du traitement mis en œuvre, de sa finalité, de la catégorie des données traitées ainsi que la durée de conservation des données.
            </p>
            <p>
                <strong> A) Nature du traitement mis en oeuvre :</strong>
                Le traitement de données personnelles que nous avons mis en œuvre pour notre application web consiste à récupérer et enregistrer des données personnelles essentielles pour notre application tels que le nom, prénom et adresse mail. De cette manière on garantit une meilleure expérience utilisateur car ce dernier n’a qu’à fournir la quantité minimale d'informations. Ces informations sont généralement celles nécessaires pour l’inscription sur n’importe quel site internet et cela permet à l’application de travailler avec une quantité de données restreinte et à terme, rendre l’application plus performante. Parallèlement, la faible quantité d’informations récoltées signifie également qu’il y aura moins de données sensibles à protéger. Cela peut réduire les risques liés à la sécurité et les préoccupations des utilisateurs concernant la confidentialité de leurs données.
            </p>
            <p>
                <strong> B) Finalité du traitement :</strong>
                Nous utilisons les données récupérées pour gérer plus facilement les comptes utilisateurs. Les données personnelles sont donc utilisées pour créer et gérer les comptes utilisateurs, savoir qui a créé un ticket sur l’application ou encore connaître les habitudes des utilisateurs et savoir pourquoi certaines machines tombent souvent en panne. Les données
                récupérées nous permettent également de gérer les autorisations pour les utilisateurs qui s’inscrivent et pour les utilisateurs qui font partie de l’équipe encadrante de la plateforme (administrateur web, administrateur système et techniciens.
            </p>
            <p>
                <strong> C) Catégorie des données traitées :</strong>
                Dans le cas de notre application, les données personnelles traitées sont destinées à
                l’identification (nom, prénom & adresse mail). L’adresse mail est d’ailleurs la donnée la plus importante puisqu’elle est utilisée pour diﬀérencier les comptes utilisateurs puisque chaque adresse mail ne peut être reliée qu’à un seul compte à la fois. Le nom et prénom sont eux utilisés uniquement pour l’attribution des tickets puisque chaque ticket a un propriétaire.
                <br>
                <br>
                Également, par la suite, nous proposerons aux clients qui le souhaitent de rentrer plus d’informations comme leur adresse, leur numéro de téléphone ainsi que leur genre. Ces données seront récoltées uniquement si les clients le souhaitent et seront utilisées à des fins statistiques. ( Cette récolte d’information n’est pas encore disponibles )
            </p>
            <p>
                <strong> D) Durée de conservation :</strong>
                Dans le cas de notre application, les données personnelles utilisées sont eﬀectivement utiles pour les l’identification des utilisateurs, les données sont gardées pendant la durée de vie du compte tant qu’il est actif. Si un utilisateur décide de supprimer son compte, les données peuvent être supprimées immédiatement ou dans un délai relativement court après une demande de suppression faite par l’utilisateur concerné. Selon la CNIL, cette durée peut atteindre 10 ans après la suppression du compte ( <a href="https://www.cnil.fr/fr/passer-laction/les-durees-de-conservation-des-donnees" style="color: #a9a9a9">CNIL - Durée</a> )
            </p>
        </div>
