                <h2 style="color: #FFFFFF">3 ) Gestion des droits des personnes concernées</h2>
                <p>
                    Nous récupérons les données personnelles des individus à des fins mentionnées plus haut. Ainsi, nous avons le devoir de nous assurer que ces individus soient en possessions de leurs droits vis à vis de leur données personnelles et en accord avec le RGPD. Les diﬀérents droits sont les suivants :
                </p>
                <ul  class="styled-list">
                    <li>
                        <strong>Le droit d'accès :</strong><br>
                        Nos utilisateurs pourront être en mesure d'accéder à leur données personnelles stockées dans l'application. Pour cela nous proposerons une page réservée au compte utilisateur dans laquelle cette action sera possible. Les utilisateurs
                        ne pourront accéder qu'aux données les concernant il ne seront pas en mesure d'accéder aux données des autres.
                    </li>
                    <li>
                        <strong>Le droit de rectification :</strong><br>
                        Les utilisateurs auront également le droit de corriger leurs données personnelles si elles sont inexactes ou s'ils changent d’adresse mail par exemple. En permettant aux utilisateurs de corriger les informations incorrectes, ils peuvent protéger leur réputation en ligne et hors ligne.                    </li>
                    <li>
                        <strong>Le droit à l'effacement : </strong><br>
                        Les utilisateurs pourront aussi demander à ce que leurs données personnelles soient supprimées de nos bases de données. Ce droit améliore la protection de la vie privée en permettant aux individus de supprimer des informations qu’ils jugent sensibles, obsolètes ou qui pourraient être utilisées de manière inappropriée. Néanmoins, si les données qui doivent être supprimées sont nécessaires à nos activités (Exemple: nom et adresse mail pour l’identification des tickets),

                        l’utilisateur pourra eﬀectivement supprimer ces données sous la contrainte de ne plus utiliser nos services.
                    </li>
                    <li>
                        <strong>Le droit à la portabilité des données :</strong><br>
                        Les utilisateurs pourront demander la récupération de leurs données personnelles sous la forme d’un document qu'ils pourront télécharger. Par ailleurs, le droit à la portabilité renforce la transparence des pratiques des entreprises en ce qui concerne le traitement des données. Les utilisateurs sont plus enclins à faire confiance à des entreprises qui respectent ce droit.
                    </li>
                    <li>
                        <strong>Le droit d'opposition : </strong><br>
                        Les utilisateurs ont le droit de s'opposer au traitement de leurs données personnelles et pourront nous le faire part par mail. Le droit d'opposition permet aux utilisateurs de contrôler la manière dont leurs données personnelles seront utilisées, renforçant ainsi le respect de leur vie privée. Cela donne aux individus un plus grand contrôle sur le traitement de leurs informations.                    </li>
                </ul>
                <p>
                    En sommes nous comptons garantir ces droits ci-dessus à chaque utilisateur en ce qui concerne leur informations personnelles.
                </p>
